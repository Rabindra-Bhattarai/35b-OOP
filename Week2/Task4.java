
/* somplete the task 1 using ternary operators
 * write a program to print whether a variable is greater or equal to 18.
 */
public class Task4 {
    public static void main(String[] args) {
        int age = 18; // replace this with the variable you want to check

        // use a ternary operator to check if the variable is greater than or equal to 18
        String result = (age >= 18)? "The variable is greater than or equal to 18." : "The variable is less than 18.";

        // print the result
        System.out.println(result);
    }
}